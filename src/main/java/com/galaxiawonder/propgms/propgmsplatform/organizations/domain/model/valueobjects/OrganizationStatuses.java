package com.galaxiawonder.propgms.propgmsplatform.organizations.domain.model.valueobjects;

/**
 * Represents the operational status of an organization.
 *
 * This enum is used to define whether an organization is currently active or inactive.
 * It provides two possible states:
 * - ACTIVE: Indicates that the organization is currently operational.
 * - INACTIVE: Indicates that the organization is not currently operational.
 */
public enum OrganizationStatuses {
    ACTIVE,
    INACTIVE
}
